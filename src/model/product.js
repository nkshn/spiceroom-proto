class Product {
  constructor(id, title, description, price, image, fryingDegree, aroma, taste) {
    this.id = id;
    this.title = title;
    this.description = description;
    this.price = price;
    this.image = image;
    this.fryingDegree = fryingDegree;
    this.aroma = aroma;
    this.taste = taste;
  }
}

export default Product;